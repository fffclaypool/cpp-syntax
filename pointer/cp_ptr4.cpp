#include <iostream>
using namespace std;

int main()
{
     const char *a[2];
     const char **p;

     a[0] = "ABC";
     a[1] = "DEF";

     cout << "a[0]=" << a[0] << "\n"; // a[0]=ABC
     cout << "a[1]=" << a[1] << "\n"; // a[1]=DEF

     p = a;
     cout << "*p    =" << *p << "\n";       // *p    =ABC
     cout << "*(p+1)=" << *(p + 1) << "\n"; // *(p+1)=DEF

     cout << "a    =" << a << "\n";            // a    =0x304dfd610
     cout << "&a[0]=" << &a[0] << "\n";        // &a[0]=0x304dfd610
     cout << "&a[1]=" << &a[1] << "\n";        // &a[1]=0x304dfd618
     cout << "a[0] =" << (void *)a[0] << "\n"; // a[0] =0x100641f24
     cout << "a[1] =" << (void *)a[1] << "\n"; // a[1] =0x100641f28

     // a[0]=A *(a[0]+1)=B *(a[0]+2)=C
     cout << "a[0]=" << *a[0] << " *(a[0]+1)=" << *(a[0] + 1)
          << " *(a[0]+2)=" << *(a[0] + 2) << "\n";
     // a[1]=D *(a[1]+1)=E *(a[1]+2)=F
     cout << "a[1]=" << *a[1] << " *(a[1]+1)=" << *(a[1] + 1)
          << " *(a[1]+2)=" << *(a[1] + 2) << "\n";

     cout << "p     =" << p << "\n";                // p     =0x30545b610
     cout << "(p+1) =" << (p + 1) << "\n";          // (p+1) =0x30545b618
     cout << "*p    =" << (void *)*p << "\n";       // *p    =0x10081bed8
     cout << "*(p+1)=" << (void *)*(p + 1) << "\n"; // *(p+1)=0x10081bedc

     // **p    =A *(*p+1)=B *(*p+2)=C
     cout << "**p    =" << **p << " *(*p+1)=" << *(*p + 1)
          << " *(*p+2)=" << *(*p + 2) << "\n";
     // **(p+1)=D *(*(p+1)+1)=E *(*(p+1)+2)=F
     cout << "**(p+1)=" << **(p + 1) << " *(*(p+1)+1)=" << *(*(p + 1) + 1)
          << " *(*(p+1)+2)=" << *(*(p + 1) + 2) << "\n";

     return 0;
}
